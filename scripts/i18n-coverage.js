#!/usr/bin/env node
'use strict';

// scripts/i18n-coverage.js — v4.47.0 NF24 locale coverage report.
//
// check-i18n.js already enforces structural parity (every EN key is
// present in every non-EN locale, no orphan keys). What it does NOT
// flag is *content drift* — a locale that ships byte-identical to EN
// for most keys is silently incomplete. The user's chrome.i18n stack
// will fall through to default_locale anyway when a translation is
// missing, so a locale file that simply mirrors EN looks structurally
// fine but offers no localisation value to non-EN speakers.
//
// This script generates a human-readable coverage report at
// `docs/i18n-coverage.md` with per-locale percentage of keys whose
// translated value differs from EN. Brand names and technical formats
// (e.g. "Astra Deck", "MP4", "VP9") are intentionally preserved as
// English; the per-locale generators in scripts/generate-locales.js
// keep them verbatim. So 100 % is unrealistic and not the goal —
// a healthy locale tends to land in the 75-95 % range. The report is
// informational, not a build gate.
//
// Run: `node scripts/i18n-coverage.js` (or `npm run i18n:coverage`).

const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.join(__dirname, '..');
const LOCALES_DIR = path.join(REPO_ROOT, 'extension', '_locales');
const REPORT_PATH = path.join(REPO_ROOT, 'docs', 'i18n-coverage.md');

function readLocale(name) {
    const filePath = path.join(LOCALES_DIR, name, 'messages.json');
    return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function listLocales() {
    return fs.readdirSync(LOCALES_DIR, { withFileTypes: true })
        .filter((d) => d.isDirectory())
        .map((d) => d.name);
}

function messageOf(entry) {
    if (!entry || typeof entry !== 'object') return '';
    return typeof entry.message === 'string' ? entry.message : '';
}

function pct(numerator, denominator) {
    if (denominator === 0) return 0;
    return Math.round((numerator / denominator) * 1000) / 10;
}

function main() {
    const locales = listLocales();
    if (!locales.includes('en')) {
        console.error('[i18n-coverage] _locales/en/messages.json is required as the reference locale');
        process.exit(1);
    }
    const en = readLocale('en');
    const enKeys = Object.keys(en);
    const enTotal = enKeys.length;

    const rows = [];
    for (const name of locales) {
        if (name === 'en') continue;
        const locale = readLocale(name);
        let translated = 0;
        let identical = 0;
        let missing = 0;
        for (const key of enKeys) {
            const enMsg = messageOf(en[key]);
            const locMsg = messageOf(locale[key]);
            if (!(key in locale)) {
                missing += 1;
            } else if (locMsg === enMsg) {
                identical += 1;
            } else {
                translated += 1;
            }
        }
        rows.push({
            name,
            translated,
            identical,
            missing,
            translatedPct: pct(translated, enTotal),
            identicalPct: pct(identical, enTotal),
            missingPct: pct(missing, enTotal),
        });
    }

    // Build the markdown report.
    const lines = [];
    lines.push('# Astra Deck — i18n Coverage');
    lines.push('');
    lines.push('Auto-generated by `node scripts/i18n-coverage.js` (or `npm run i18n:coverage`). Do not hand-edit.');
    lines.push('');
    lines.push(`Reference locale: **en** (${enTotal} keys).`);
    lines.push('');
    lines.push('| Locale | Translated | Identical-to-EN | Missing | Coverage |');
    lines.push('| --- | ---:| ---:| ---:| ---:|');
    for (const r of rows) {
        lines.push(
            `| ${r.name} | ${r.translated} (${r.translatedPct}%) | ${r.identical} (${r.identicalPct}%) | ${r.missing} (${r.missingPct}%) | ${r.translatedPct.toFixed(1)}% |`
        );
    }
    lines.push('');
    lines.push('## Reading the columns');
    lines.push('');
    lines.push('- **Translated** — keys whose value is **different** from the EN message. This is the true coverage signal.');
    lines.push('- **Identical-to-EN** — keys that ship byte-identical to EN. Some are intentional (brand names: *Astra Deck*, *Astra Downloader*, *DeArrow*, *SponsorBlock*, *YouTube*; technical formats: *MP4*, *M4A*, *VP9*, *AV1*, *H.264*, *TrustedTypes*, *ETA*). Others may be untranslated placeholders awaiting native-speaker review.');
    lines.push('- **Missing** — keys absent from the locale file. `chrome.i18n.getMessage` will fall through to EN for these.');
    lines.push('- **Coverage** — same as the Translated percentage; restated as the single headline number.');
    lines.push('');
    lines.push('## How to improve coverage');
    lines.push('');
    lines.push('1. Open `scripts/generate-locales.js` and find the locale\'s `T[\'<locale>\']` translation table.');
    lines.push('2. Add an entry mapping the English message to the translated message. Brand names + technical formats should NOT be translated.');
    lines.push('3. Re-run `node scripts/generate-locales.js` to rewrite `extension/_locales/<locale>/messages.json`.');
    lines.push('4. Re-run `node scripts/i18n-coverage.js` to refresh this report.');
    lines.push('5. For zh_CN, the locale is hand-maintained (out of the generator); edit `extension/_locales/zh_CN/messages.json` directly.');
    lines.push('');

    fs.writeFileSync(REPORT_PATH, lines.join('\n') + '\n', 'utf8');

    // Echo a tiny summary to stdout so the user sees the result.
    console.log(`[i18n-coverage] wrote ${path.relative(REPO_ROOT, REPORT_PATH).split(path.sep).join('/')} — ${rows.length} locale(s) profiled against ${enTotal} EN keys`);
    for (const r of rows) {
        console.log(`  ${r.name.padEnd(8)}  ${r.translatedPct.toFixed(1).padStart(5)}% translated  (${r.identical} identical-to-EN, ${r.missing} missing)`);
    }
}

main();
